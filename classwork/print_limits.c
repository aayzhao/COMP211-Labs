#include <stdio.h>
#include <stdlib.h>
#include <limits.h>

int main()
{
    printf("\n\n");
    
    printf("Integer min: \t\t\t%d\n", INT_MIN);
    printf("Integer max: \t\t\t %d\n", INT_MAX);
    printf("Unsigned Integer max: \t\t %u\n", UINT_MAX);
    printf("Long Integer min: \t\t%ld\n", LONG_MIN);
    printf("Long Integer max: \t\t %ld\n", LONG_MAX); 
    printf("Unsigned Long Integer max: \t %lu\n", ULONG_MAX);
    
    printf("\n\n");

    return 0;
}
